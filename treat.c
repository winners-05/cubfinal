#include "cub3d.h"

void		treat_virgul(char *str)
{
int comma;
comma = 0 ;
while(*str)
{
	if (*str == ',')
	{
		comma++;
		if (comma > 2)
			error_gestion(3);
	}
	str++;
}
}
